function zeroadez(num) {
  switch (num) {
    case 0:
      console.log("O numero é zero");
      break;
    case 1:
      console.log("O numero é um");
      break;
    case 2:
      console.log("O numero é dois");
      break;
    case 3:
      console.log("O numero é tres");
      break;
    case 4:
      console.log("O numero é quatro");
      break;
    case 5:
      console.log("O numero é cinco");
      break;
    case 6:
      console.log("O numero é seis");
      break;
    case 7:
      console.log("O numero é sete");
      break;
    case 8:
      console.log("O numero é oito");
      break;
    case 9:
      console.log("O numero é nove");
      break;
    case 10:
      console.log("O numero é dez");
      break;
    default:
      console.log("Numero fora do intervalo");
  }
}
zeroadez(0);
zeroadez(1);
zeroadez(2);
zeroadez(3);
zeroadez(4);
zeroadez(5);
zeroadez(6);
